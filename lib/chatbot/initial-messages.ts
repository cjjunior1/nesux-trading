/**
 * Obtiene un mensaje inicial ALEATORIO, garantizando que nunca repita
 * el mismo mensaje en una sesión
 */
export function getRandomInitialMessage(usedMessages: Set<string> = new Set()): string {
  const availableMessages = INITIAL_MESSAGES.filter(msg => !usedMessages.has(msg));
  
  if (availableMessages.length === 0) {
    // Si ya usamos todos, reseteamos
    return INITIAL_MESSAGES[Math.floor(Math.random() * INITIAL_MESSAGES.length)];
  }
  
  return availableMessages[Math.floor(Math.random() * availableMessages.length)];
}

